## Comparison expressions

# equals ==
if 5 == 5:
    print( "5 == 5 " )

if 5 == 6:
    print( "5 == 6"  )

# not equal to !=
if 9 != 7:
    print( "9 != 7 "  )

if 9 != 9:
    print( "9 != 9 "  )


# less than <
if 5 < 6 :
    print( "5 < 6 "  )
    
print( f"5 < 5 : { 5 < 5 }"  )
print( f"5 < 4 : { 5 < 4 }  \n"  )


# greater than >
print( f"5 > 4 : { 5 > 4 }"  )

print( f"5 > 5 : { 5 > 5 }"  )

print( f"5 > 6 : { 5 > 6 }  \n"  )


# less or equal than >=

print( f"5 <= 5 : { 5 <= 5 }  "  )

print( f"5 <= 6 : { 5 <= 6 }  "  )

print( f"5 <= 4 : { 5 <= 6 }  \n"  )


# greater or equal than >=
if 5 >= 5 :
    print( "5 >= 5"  )

if 5 >= 4 :
    print( "5 >= 4"  )

if 5 >= 6 :
    print( "5 >= 6"  )
